# 산술연산자 : +, - ,*, / ,%
x = 10
y = 20
print("x+y=", x+y, end='\n')
print("x-y=", x-y, end='\n')
print("x*y=", x*y, end='\n')
print("x/y=", x/y, end='\n')
print("x//y=", x//y, end='\n')  # 몫만
print("x%y=", x % y, end='\n')
print("2**2=", 2**2, end='\n')  # 제곱

# print("서로다른 타입의 자료형 연산"+2)  # type error

